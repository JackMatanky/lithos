async function nl_time(tp, prompt) {
  const prompt_arg = `${prompt}`;

  let suggester_prompt;
  if (prompt_arg != "") {
    suggester_prompt = prompt_arg;
  } else {
    suggester_prompt = "Start Time?";
  }
  const nl_time_obj = await tp.system.suggester(
    (item) => item.key,
    nl_time_obj_arr,
    false,
    `${suggester_prompt}`
  );

  let time = "null";
  if (nl_time_obj) {
    time = app.plugins.plugins["nldates-obsidian"]
      .parseDate(nl_time_obj.value)
      .moment.format("HH:mm");
  }

  return time;
}

