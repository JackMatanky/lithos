// VAR: TYPE OPTIONS: "start", "end", ""
async function nl_date(tp, type) {
  const date_type = `${type}`;

  let suggester_prompt;
  if (date_type == 'start') {
    suggester_prompt = 'Start Date?';
  } else if (date_type == 'end') {
    suggester_prompt = 'End Date?';
  } else if (date_type != '') {
    suggester_prompt = date_type;
  } else {
    suggester_prompt = 'When is the date?';
  }

  const nl_date_obj = await tp.system.suggester(
    (item) => item.key,
    date_obj_arr,
    false,
    suggester_prompt,
    419
  );

  const nl_date_value = nl_date_obj.value;

  let date = nl_date_value;
  if (nl_date_value != 'null') {
    date = app.plugins.plugins['nldates-obsidian']
      .parseDate(nl_date_value)
      .moment.format('YYYY-MM-DD');
  }

  return date;
}

