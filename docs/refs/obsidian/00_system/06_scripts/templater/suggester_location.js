async function suggester_location({
  tp,
  country: country_param,
  city: city_param,
  utc_dst: utc_dst_param,
}) {
  const country_arg = country_param;
  const city_arg = city_param;

  let country_name = "Null";
  let country_value = "null";
  let city_name = "Null";
  let city_value = "null";
  let utc_value = "null";
  let dst_value = "null";

  const country_obj_key = country_obj_arr.map((country) => country.key);
  const country_obj_value = country_obj_arr.map((country) => country.value);
  if (country_arg == "input" || country_arg == true) {
    let country_obj = await tp.system.suggester(
      (item) => item.key,
      country_obj_arr,
      false,
      "Country?"
    );
    country_name = country_obj.key;
    country_value = country_obj.value;
  } else if (country_obj_value.includes(country_arg)) {
    country_value = country_arg;
    country_name = country_obj_arr
      .filter((country) => country.value == country_value)
      .map((country) => country.key)
      .toString();
  } else if (country_obj_key.includes(country_arg)) {
    country_name = country_arg;
    country_value = country_obj_arr
      .filter((country) => country.key == country_name)
      .map((country) => country.value)
      .toString();
  }

  if (city_arg == "input" || city_arg == true) {
    if (country_arg == "input" || country_arg == true) {
      city_obj_arr = city_obj_arr.filter(
        (city) => city.country == "null" || city.country == country_value
      );
    } else if (
      country_arg != false &&
      country_arg != null &&
      country_arg != "null"
    ) {
      city_obj_arr = city_obj_arr.filter(
        (city) => city.country == "null" || city.country == country_arg
      );
    }
    let city_obj = await tp.system.suggester(
      (item) => item.key,
      city_obj_arr,
      false,
      "City?"
    );
    city_name = city_obj.key;
    city_value = city_obj.value;
    utc_value = city_obj.utc;
    dst_value = city_obj.dst;
  } else if (city_arg != false && city_arg != null && city_arg != "null") {
    city_name = "Null";
    city_value = "null";
    utc_value = "null";
    dst_value = "null";
  }
  if (city_value == "_user_input") {
    city_name = await tp.system.prompt("City?");
    city_value = city_name
      .replaceAll(/,/g, "")
      .replaceAll(/\s/g, "_")
      .replaceAll(/\//g, "-")
      .replaceAll(/&/g, "and")
      .toLowerCase();
    if (
      utc_dst_param == false ||
      utc_dst_param == null ||
      utc_dst_param == "null"
    ) {
      utc_value = "null";
      dst_value = "null";
    } else {
      utc_dst_obj = await tp.system.suggester(
        (item) => item.key,
        utc_dst_obj_arr,
        false,
        "UTC Offset?"
      );
      utc_value = utc_dst_obj.utc;
      dst_value = utc_dst_obj.dst;
    }
  }

  location_obj = {
    country_key: country_name,
    country_value: country_value,
    city_key: city_name,
    city_value: city_value,
    utc: utc_value,
    dst: dst_value,
  };

  return location_obj;
}

